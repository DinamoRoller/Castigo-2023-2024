public class Main {
    public static void main(String[] args) {

        char maxChar = Character.MAX_VALUE;
        short maxShort = Short.MAX_VALUE;
        int maxInt = Integer.MAX_VALUE;

        System.out.println("Numero maximo por un char: " + (int)maxChar);
        System.out.println("Numero maximo por un short: " + maxShort);
        System.out.println("Numero maximo por un int: " + maxInt);
    }
}